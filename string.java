package com.company;

public class string
{
    public static void main(String[] args)
    {
        String h = "the world";
        String firstName = "John";
        String lastName = "Doe";
        System.out.println(firstName + " " + lastName);
        System.out.println(h.length());
        System.out.println(h.toUpperCase());
        System.out.println(h.toLowerCase());
        System.out.println(h.indexOf("world"));
        System.out.println(firstName.concat(lastName));
        System.out.println("jirattanun\nleeudomwong");
        System.out.println("jirattanun\rleeudonwong");
        System.out.println("jirattanun\tleeudonwong");
        System.out.println("jirattanun\bleeudomwong");
        System.out.println("jirattanun\fleeudomwong");

    }
}
